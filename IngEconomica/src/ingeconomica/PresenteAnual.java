    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        /*Calculando valor presente anual*/
        
        double val1, val2, val3, PresenteAnual;
        double a = Double.parseDouble(A.getText());
        double i = Double.parseDouble(I.getText());
        double n = Double.parseDouble(N.getText());
        
        val1 = (1+i) ;
        val2 = Math.pow(val1,n);
        val3 = i*Math.pow((1+i),n);
        
        PresenteAnual=a*(val2/val3);
        
        JOptionPane.showMessageDialog(null, "Resultado: "+PresenteAnual);
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

